package com.example.devops_tp1;

public class Dates {
    private String begin;
    private String end;

    public Dates() { }

    public String getBegin() {
        return begin;
    }

    public void setBegin(String begin) {
        this.begin = begin;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }
}
